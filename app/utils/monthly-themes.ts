export const getCurrentMonthTheme = (randomIndex: number) => {
  const currentMonth = new Date().getMonth() + 1; // JavaScript months are 0-indexed
  const monthTheme = MonthlyThemes[currentMonth as keyof typeof MonthlyThemes];

 
  // Always select the first message instead of randomly selecting
  return {
    name: monthTheme.name,
    message: monthTheme.messages[randomIndex],
    background: monthTheme.background,
    icon: monthTheme.icon
  };
};

